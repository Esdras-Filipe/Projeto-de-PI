    private void excluir(String id) {
        String sql = "delete from fornecedor where cnpj=? ";

        int input = JOptionPane.showConfirmDialog(null, "Deseja excluir fornecedor de C.N.P.J: " + id + "", "Confirme", JOptionPane.YES_NO_OPTION);
        if (input == 0) {

            try {

                pst = conexao.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Não foi possivel fazer delete no banco" + e);
            }

        }

    }

    private String consultar() {
        String sql = "select * from fornecedor where cnpj=?";
        try {

            pst = conexao.prepareStatement(sql);
            pst.setString(1, jTextField3.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                int input = JOptionPane.showConfirmDialog(null, "Deseja ver informações do fornecedor ?", "Confirme", JOptionPane.YES_NO_OPTION);
                if (input == 0) {
                    JOptionPane.showMessageDialog(null, "Código: " + rs.getString(1)
                            + "\n Nome: " + rs.getString(2)
                            + "\n C.N.P.J: " + rs.getString(3)
                            + "\n UF: " + rs.getString(4)
                            + "\n Endereço: " + rs.getString(5)
                            + "\n CEP: " + rs.getString(6));
                    return "existe";
                } else {
                    return "existe";
                }
            } else {
                return "não existe";
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
        return "consulta realizada ";
    }

    private void inserir(String identrega, String nome, String cnpj, String uf, String endereco, String cep) {
        String sql = "insert into fornecedor(identrega,nome,cnpj,UF,endereco,cep) values(?,?,?,?,?,?)";
        try {

            pst = conexao.prepareStatement(sql);
            pst.setString(1, identrega);
            pst.setString(2, nome);
            pst.setString(3, cnpj);
            pst.setString(4, uf);
            pst.setString(5, endereco);
            pst.setString(6, cep);
            int ner = pst.executeUpdate();

            if (ner == 1) {
                JOptionPane.showMessageDialog(null, "Fornecedor" + nome + " cadastrado com sucesso! ");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível cadastrar o fornecedor" + nome);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possivel fazer insert no banco" + e);
        }
    }

