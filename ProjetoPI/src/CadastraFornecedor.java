    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String consulta;
        String id = jTextField3.getText();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um C.N.P.J para exclusão");
        } else {
            consulta = consultar();
            if (consulta.equals("existe")) {
                excluir(id);
            } else {
                JOptionPane.showMessageDialog(null, "C.N.P.J informado não existe ");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        String nome = jTextField2.getText();
        String cnpj = jTextField3.getText();
        String uf = jTextField4.getText();
        String endereco = jTextField5.getText();
        String cep = jTextField6.getText();

        try {
            if (id.isEmpty() || (nome.isEmpty()) || (cnpj.isEmpty()) || (uf.isEmpty()) || (endereco.isEmpty()) || (cep.isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos ");
            } else {
                inserir(id, nome, cnpj, uf, endereco, cep);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

